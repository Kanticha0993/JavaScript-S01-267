//แยกตัวเลขเป็นตัวเลขย่อยๆ
function numberSplit(num){
    return num.toString().split('').map(Number);
}
const result = numberSplit(12345);
console.log(result);
/*
1)num.toString(): แปลงตัวเลข num ให้กลายเป็นสตริง 
เช่น 12345 จะกลายเป็น "12345".
2).split(''): แยกสตริงออกเป็นอาร์เรย์ของอักขระ 
เช่น "12345" จะกลายเป็น ['1', '2', '3', '4', '5'].
3).map(Number): แปลงอักขระแต่ละตัวในอาร์เรย์ให้เป็นตัวเลข 
เช่น ['1', '2', '3', '4', '5'] จะกลายเป็น [1, 2, 3, 4, 5].
*/